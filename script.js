//string Data type
let username = "Haritha";
console.log(username, typeof username);

//number data type
let age = 25;
console.log(age, typeof age);

//boolean data type
let Male=true;
console.log(Male, typeof Male);

//undefined data type
let undef= undefined;
console.log(undef, typeof undef);

//null data type
let nul=null;
console.log(nul, typeof nul);  

//object data type
const student = {
  name: "Haritha",
  age: 25,
  gender: "Male"
};
console.log(student.name ,  typeof student);
console.log(student.age , typeof student);
console.log(student.gender , typeof student);
